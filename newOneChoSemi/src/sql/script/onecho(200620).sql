create or replace view V_STATISTIC
as
select "newVisit","dBefore","cancel","newOrder","newMember"
,"allMember","newSale","weekSale","amonthSale","newQna","unAnsw"
from(
select count(distinct(visitor_ip||visitor_id)) "newVisit"
from visitor where to_char(day,'YYYY-MM-DD')=to_char(sysdate,'YYYY-MM-DD'))
cross join(
select count(order_no) "dBefore" 
from orderlist where delivery_code='D1')
cross join(select count(order_no) "cancel"
from orderlist where order_cancelrequest='Y')
cross join(select count(distinct(order_no)) "newOrder"
from orderlist where to_char(order_date,'YYYY-MM-DD')=to_char(sysdate,'YYYY-MM-DD'))
cross join(select count(distinct(member_no)) "newMember" 
from member where to_char(member_joindate,'YYYY-MM-DD')=to_char(sysdate,'YYYY-MM-DD'))
cross join(select count(member_no) "allMember"
from member)
cross join(select nvl(sum(item_price),0) "newSale"
from orderlist o
left join item i on (o.item_no=i.item_no)
where to_char(order_date,'YYYY-MM-DD')=to_char(sysdate,'YYYY-MM-DD'))
cross join(select sum(item_price) "weekSale"
from orderlist o
left join item i on (o.item_no=i.item_no)
where order_date between to_char(trunc(sysdate,'iw')-7,'YYYY-MM-DD') and sysdate)
cross join(select sum(item_price) "amonthSale"
from orderlist o
left join item i on (o.item_no=i.item_no)
where order_date between to_char(add_months(sysdate,-1),'YYYY-MM-DD') and sysdate)
cross join(select count(distinct(qna_no)) "newQna"
from qna where to_char(qna_cdate,'YYYY-MM-DD')=to_char(sysdate,'YYYY-MM-DD'))
cross join(select count(distinct(qna_no)) "unAnsw"
from qna where qna_yn='N');

